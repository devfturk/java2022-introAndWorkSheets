
public class Customer {
	private int id;
	private String customerNumberString;
	private String phoneString;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getCustomerNumberString() {
		return customerNumberString;
	}

	public void setCustomerNumberString(String customerNumberString) {
		this.customerNumberString = customerNumberString;
	}

	public String getPhoneString() {
		return phoneString;
	}

	public void setPhoneString(String phoneString) {
		this.phoneString = phoneString;
	}

}
